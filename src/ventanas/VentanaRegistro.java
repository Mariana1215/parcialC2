    private void btnRegistroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistroActionPerformed
        // TODO add your handling code here:
        if(txtNombre.getText().isEmpty() || txtCedula.getText().isEmpty() || txtTelefono.getText().isEmpty()
                || txtPalabraClave.getText().isEmpty()){
            JOptionPane.showMessageDialog(null,"LLenar todos los campos");
            return;
        }
        String nombre = txtNombre.getText();
        String cedula = txtCedula.getText();
        String telefono = txtTelefono.getText();
        String palabraClave = txtPalabraClave.getText();
        
        Cliente cliente = new Cliente(nombre, cedula, telefono);
        controlador.asignarCarpa(carpa, cliente, palabraClave);
        JOptionPane.showMessageDialog(null,"Carpa asignada");
        VentanaBotones ventana = new VentanaBotones();
        ventana.setVisible(true);
        this.dispose();
        
    }//GEN-LAST:event_btnRegistroActionPerformed

