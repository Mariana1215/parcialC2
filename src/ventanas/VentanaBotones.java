    @Override
    public void actionPerformed(ActionEvent e) {
        for (int i = 0; i < botones.length; i++) {
            for (int j = 0; j < botones[i].length; j++) {
                if (e.getSource().equals(botones[i][j])) {
                    Carpa carpa = controlador.obtenerCarpa(i, j);
                    if (carpa.getEstado().equals(Carpa.DISPONIBLE)) {
                        VentanaRegistro ventana = new VentanaRegistro(carpa);
                        ventana.setVisible(true);
                        this.dispose();
                    } else if (carpa.getEstado().equals(Carpa.OCUPADA)) {
                        VentanaInfo ventana = new VentanaInfo(carpa);
                        ventana.setVisible(true);
                        this.dispose();

                    }
                }
            }
        }
    }
