    public void cargarBotones() {
        int ancho = 45;
        int alto = 45;
        int texto = 1;
        for (int i = 0; i < botones.length; i++) {
            for (int j = 0; j < botones[i].length; j++) {
                botones[i][j] = new JButton();
                int separadoX = (j > 1) ? 40 : 20;
                separadoX = (j > 3) ? 70 : separadoX;
                
                
                int separadoY = (i > 0) ? 30 : 20;
                separadoY = (i > 1) ? 40 : separadoY;
                
                botones[i][j].setBounds(
                        (j * ancho + separadoX),
                        (i * alto + separadoY),
                        ancho, alto);
                botones[i][j].setText(String.valueOf(texto));
                botones[i][j].addActionListener(this);
                panelCarpas.add(botones[i][j]);
                texto++;
            }
        }

    }

    public void validarPosiciones() {
        for (int i = 0; i < botones.length; i++) {
            for (int j = 0; j < botones[i].length; j++) {
                Carpa carpa = controlador.obtenerCarpa(i, j);
                if (carpa.getEstado().equals(Carpa.DISPONIBLE)) {
                    botones[i][j].setBackground(Color.WHITE);
                } else if (carpa.getEstado().equals(Carpa.OCUPADA)) {
                    botones[i][j].setBackground(Color.BLUE);
                }
            }
        }
    }

