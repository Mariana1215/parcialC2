    private void btnPalabraClaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPalabraClaveActionPerformed
        // TODO add your handling code here:
        String llave = JOptionPane.showInputDialog("Ingrese la clave");

        if (llave != null) {
            if (llave.equals(carpa.getPalabraClave())) {
                JOptionPane.showMessageDialog(null, "Clave correcta");
            }else{
                JOptionPane.showMessageDialog(null,"Clave incorrectar");
            }
        }else{
            JOptionPane.showMessageDialog(null, "No se ingreso la clave");
        }
    }//GEN-LAST:event_btnPalabraClaveActionPerformed
    private void llenarTabla() {
        DefaultTableModel modelo = new DefaultTableModel();
        LSE<Cliente> clientes = carpa.getClientes();
        modelo.setColumnIdentifiers(new Object[]{
            "Nombre", "Cédula", "Telefono"
        });
        tabla.setModel(modelo);
        for (int i = 0; i < clientes.size(); i++) {
            modelo.addRow(new Object[]{
                clientes.get(i).getNombre(),
                clientes.get(i).getCedula(),
                clientes.get(i).getTelefono()

            });
        }
    }

    private void limpiarCampos() {
        txtNombre.setText("");
        txtCedula.setText("");
        txtTelefono.setText("");

    }
